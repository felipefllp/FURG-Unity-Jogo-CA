            death = false;
            Time.timeScale = .25f;
            Invoke("NextLvl", resetDelay);
        }
        if (lives < 1) {
            gameOver.SetActive(true);
            bg.SetActive(true);
            Time.timeScale = .25f;
            Cursor.visible = true;
            Invoke("Reset", resetDelay);
        }

    }

    void Reset() {
        Time.timeScale = 1f;
        death = true;
        SceneManager.LoadScene(0);
    }

    void NextLvl() {
        Time.timeScale = 1f;
        death = true;
        SceneManager.LoadScene(SceneManager.GetActiveScene().buildIndex + 1);
    }

    public void LoseLife() {
        if (death) { 
            lives--;
            livesText.text = "Vidas: " + lives;

            Destroy(clonePaddle);
            CheckGameover();
            Invoke("SetupPaddle", resetDelay + .25f);
        }
    }

    void SetupPaddle() {
        clonePaddle = Instantiate(pad1, new Vector2(0, -16.70597f), Quaternion.identity) as GameObject;
        mousee.SetActive(true);
    }


    // Update is called once per frame
    void Update() {
        if (Input.GetButtonDown("Fire1")) {
            mousee.SetActive(false);
        }

        if (Input.GetKey("escape")) {
            Application.Quit();
        }

        if (Input.GetKeyDown(KeyCode.P)) {
            youWon.SetActive(true);
            bg.SetActive(true);
            death = false;
            Time.timeScale = .25f;
            Invoke("NextLvl", .25f);
        }
    }
